			var cate_pop = document.getElementsByClassName('cate_pop')[0];
			var cate_part = cate_pop.getElementsByClassName('cate_part');
			J_cate.onmouseover = function(e){
				var e = e|| window.event;
				var target = e.target;
				if(target.className == 'J_cate_menu_item'){
					removeTagClass(J_cate_menu,' J_cate_menu_item_on','li');
					target.className += ' J_cate_menu_item_on';
					cate_pop.style.display = 'block';
					var index = target.getAttribute('data-index') -1;
					tab_sub_H_and_S(cate_pop,'cate_part',index);
				}
			}
			J_cate.onmouseleave = function(e){
				var e = e|| window.event;
				var target = e.target;
				removeTagClass(J_cate_menu,' J_cate_menu_item_on','li');
				cate_pop.style.display = 'none';
				subHidden(cate_pop,'cate_part');
			}
			
			
