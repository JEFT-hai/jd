			function renderItem(obj){

				var cate_part = document.createElement('div');
				cate_part.className = 'cate_part clearfix';

				var cate_part_col1 = document.createElement('div');
				cate_part_col1.className = 'cate_part_col1';

				var cate_part_col2 = document.createElement('div');
				cate_part_col2.className = 'cate_part_col2';

				cate_part.appendChild(cate_part_col1);
				cate_part.appendChild(cate_part_col2);
				for(var key in obj){

					if(key == 'channel'){

						var cate_channel = document.createElement('div');
						cate_channel.className = 'cate_channel';

						append(cate_channel,obj[key]);

						cate_part_col1.appendChild(cate_channel);
						
					}else if(key == 'detail'){

						var cate_detail = document.createElement('div');
						cate_detail.className = 'cate_detail';

						for(var key1 in obj.detail){
							var dl = document.createElement('dl');

							for(var key2 in obj.detail[key1]){
								var dt = document.createElement('dt');
								var dd = document.createElement('dd');
								if(key2 == 'dt'){
									append(dt,obj.detail[key1][key2]);
									dl.appendChild(dt);
								}else{
									append(dd,obj.detail[key1][key2]);
									dl.appendChild(dd);
								}
							}

							cate_detail.appendChild(dl);
						}

						cate_part_col1.appendChild(cate_detail);

					}else if(key == 'imglist'){
						for(var key3 in obj.imglist){

							if(key3 == 'brand'){
								var cate_brand = document.createElement('div');
								cate_brand.className = 'cate_brand';
								appendImg(cate_brand,obj.imglist[key3],'jd/cate_brand_');

								cate_part_col2.appendChild(cate_brand);
							}else{
								var cate_promotion = document.createElement('div');
								cate_promotion.className = 'cate_promotion';
								appendImg(cate_promotion,obj.imglist[key3],'jd/cate_promotion_');

								cate_part_col2.appendChild(cate_promotion);
							}	
						}
					}
				}

				cate_pop.appendChild(cate_part);
			}
			

			function append(elem,obj){
				for(var i=0,len=obj.length;i<len;i++){
					var a = document.createElement('a');
					a.innerHTML = obj[i];
					a.href = '#';
					elem.appendChild(a);
				}
			}

			function appendImg(elem,obj,prev){
				for(var i=0,len=obj.length;i<len;i++){
					var a = document.createElement('a');
					var img = document.createElement('img');
					img.src = prev+obj[i];
					a.href = '#';
					a.appendChild(img);
					elem.appendChild(a);
				}
			}


