	var selectPicker = {};
	
	
	var select_picker = document.getElementsByClassName('select_picker');

	selectPicker.init = function(elem){

		var ui_selcetPicker_wrapper = document.getElementsByClassName('ui_selcetPicker_wrapper')[0];
		if(!ui_selcetPicker_wrapper){
			var ui_selcetPicker_wrapper = document.createElement('div');
			var ui_selectPicker = document.createElement('div');
			ui_selcetPicker_wrapper.classList.add('ui_selcetPicker_wrapper');
			document.body.appendChild(ui_selcetPicker_wrapper);
		}else{
			var ui_selectPicker = document.getElementsByClassName('ui_selectPicker')[0];
		};

			ui_selcetPicker_wrapper.style.display = 'block';

		if(elem.classList.contains('gnjp_city_list')){
			
			ui_selectPicker.className = 'gnjp_citylist_picker ui_selectPicker';

			selectPicker.data = gnjpCityListdata;
		}else if(elem.classList.contains('gjjp_city_list')){

			ui_selectPicker.className = 'gjjp_city_list ui_selectPicker';

			selectPicker.data = gjjpCityListdata;
		}else if(elem.classList.contains('gamesDK_list')){

			ui_selectPicker.className = 'gamesDK_list_picker ui_selectPicker';

			selectPicker.data = yxdkListdata;
		}

		// console.log(selectPicker.data);
		ui_selectPicker.innerHTML = selectPicker.render(selectPicker.data);
		ui_selcetPicker_wrapper.appendChild(ui_selectPicker);

		var ui_selectList_head= ui_selcetPicker_wrapper.getElementsByClassName('ui-selectList_head')[0];
		var ui_selectList_headList = ui_selectList_head.getElementsByTagName('li');

		var ui_selectList_content= ui_selcetPicker_wrapper.getElementsByClassName('ui-selectList_content')[0];
		var ui_selectList_contentList = ui_selectList_content.getElementsByClassName('ui-tab_list');

		for(var i = 0;i<ui_selectList_headList.length;i++){
			ui_selectList_headList[i].dataset.index = i;
			ui_selectList_contentList[i].style.display = 'none';
			if(i == selectPicker.target.index){
				ui_selectList_headList[i].classList.add('cur');
				ui_selectList_contentList[i].style.display = 'block';
			}
			
		}

		ui_selectList_head.addEventListener('click',function(e){
			var e = e || window.event;
			var target = e.target;

			if(target.nodeName.toLowerCase() == 'li'){
				var index = target.dataset.index;
				selectPicker.target.index = index;

				for(var i = 0;i<ui_selectList_headList.length;i++){
					if(ui_selectList_headList[i].classList.contains('cur')){
						ui_selectList_headList[i].classList.remove('cur');
						ui_selectList_contentList[i].style.display = 'none';
					}
					if(i == selectPicker.target.index){
						ui_selectList_headList[i].classList.add('cur');
						ui_selectList_contentList[i].style.display = 'block';
					}
					
				}

			}
		},false);

		ui_selectList_content.addEventListener('click',function(e){
			var e = e||window.event;
			var target = e.target;

			if(target.nodeName.toLowerCase() == 'a'){
				var val = target.innerHTML;
				selectPicker.target.value = val;

				ui_selcetPicker_wrapper.style.display = 'none';

				if(selectPicker.target.dataset.next){
					var nextID = document.getElementById(selectPicker.target.dataset.next);
					nextID.focus(); 
				}
				

			}
		},false);

		ui_selcetPicker_wrapper.addEventListener('mouseleave',function(e){
			var e = e || window.event;

			if(e.relatedTarget.classList.contains('select_picker')){
				return;
			}else{
				ui_selcetPicker_wrapper.style.display = 'none';
				elem.blur();
			}

		},false);
		
	}
	selectPicker.render = function(dataList){
		var html = '<div class="ui-selectList_title">支持中文/拼音/简拼输入</div>'+
			'<ul class="ui-selectList_head">';
			for(var key in dataList){
				html += '<li>'+ key +'</li>'
			};
				

		html +=	'</ul>'+
				'<div class="ui-selectList_content">';

			for(var key in dataList){
				html += '<div class="ui-tab_list">'
				for(var key1 in  dataList[key]){
					html += '<div class="item">'
					if(dataList[key][key1].fl){
						html += '<div class="fl">'+ dataList[key][key1].fl+ '</div>'		
					}
						html +=	'<div class="fn">'
					for(var i = 0;i<dataList[key][key1].fn.length;i++){
						html += '<a href="javascript:;">'+dataList[key][key1].fn[i]+'</a>'
					}
					html +=	'</div>'+
						'</div>'

				}
				html +=	'</div>'			
		}

		//console.log(dataList)
		//console.log(html,33);

		return html;
	}

